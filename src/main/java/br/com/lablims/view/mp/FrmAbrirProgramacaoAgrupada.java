    public final void readLaboratorio() {
        try {
            DefaultListModel model = new DefaultListModel();
            SetorDAO setorDAO = new SetorDAO();
            for (Setor setor : setorDAO.readSetorLaboratorio()) {
                model.addElement(setor.getSiglaSetor());
            }
            lstEquipamentos.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao abrir");
        }
    }

    private void abrir() {
        setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        String title = lstEquipamentos.getSelectedValue().toString();
        try {
            if (frm == null || !frm.isVisible()) {
                frm = new FrmProgramacaoAgrupada();
                criarJanela(title, new PnlProgramacaoAgrupada(title));
                frm.setVisible(true);
            } else if (frm.isVisible() == true) {
                criarJanela(title, new PnlProgramacaoAgrupada(title));
                frm.toFront();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao abrir" + e);
        }
        dispose();
        setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
    }

    public void criarJanela(String title, JPanel pnl) {
        ImageIcon icone = new ImageIcon(getClass().getResource("/Imagem/icon_cancel.png"));
        try {
            if (tblProgramacaoLaboratorio.indexOfTab(title) == -1) {
                tblProgramacaoLaboratorio.addTab(title, pnl);
                int index = tblProgramacaoLaboratorio.indexOfTab(title);

                JPanel pnlTab = new JPanel(new GridBagLayout());
                pnlTab.setOpaque(false);
                JLabel lblTitle = new JLabel(title);
                JButton btnClose = new JButton();
                btnClose.setIcon(icone);
                btnClose.setContentAreaFilled(false);
                btnClose.setPreferredSize(new Dimension(25, 25));
                btnClose.setHorizontalTextPosition(SwingConstants.RIGHT);
                lblTitle.setBorder(BorderFactory.createEmptyBorder(0, 0, 0, 40));
                lblTitle.setFont(new Font(Font.SANS_SERIF, Font.BOLD, 12));

                GridBagConstraints gbc = new GridBagConstraints();
                gbc.gridx = 0;
                gbc.gridy = 0;
                gbc.weightx = 1;

                pnlTab.add(lblTitle, gbc);

                gbc.gridx++;
                gbc.weightx = 0;
                pnlTab.add(btnClose, gbc);

                btnClose.addActionListener(new ActionListener() {
                    @Override
                    public void actionPerformed(ActionEvent e) {
                        int dialogResult = JOptionPane.showConfirmDialog(null,
                                "Deseja fechar essa janela?", "Aviso", JOptionPane.YES_NO_OPTION);
                        if (dialogResult == 0) {
                            int index = tblProgramacaoLaboratorio.indexOfTab(title);
                            if (index >= 0) {
                                tblProgramacaoLaboratorio.remove(index);
                                setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
                            }
                        }
                    }
                });
                btnClose.addMouseListener(new MouseListener() {
                    @Override
                    public void mouseClicked(MouseEvent evt) {
                    }

                    @Override
                    public void mousePressed(MouseEvent evt) {
                    }

                    @Override
                    public void mouseReleased(MouseEvent evt) {
                    }

                    @Override
                    public void mouseEntered(MouseEvent evt) {
                        setCursor(Cursor.getPredefinedCursor(Cursor.HAND_CURSOR));
                    }

                    @Override
                    public void mouseExited(MouseEvent evt) {
                        setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
                    }
                });
                tblProgramacaoLaboratorio.setTabComponentAt(index, pnlTab);
                tblProgramacaoLaboratorio.setSelectedIndex(tblProgramacaoLaboratorio.indexOfTab(title));
            } else {
                tblProgramacaoLaboratorio.setSelectedIndex(tblProgramacaoLaboratorio.indexOfTab(title));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro criar a Janela: " + title);
        }

    }

