    private void carregar() {
        setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        try {
            LoteMateriaPrima loteMP = new LoteMateriaPrimaDAO().findById(LoteMateriaPrima.class, lote.getId());
            Material mat = new MaterialDAO().findMaterialMetodologiaById(loteMP.getMaterial().getId());
            material = new MaterialDAO().findMaterialMetodologiaById(mat.getId());
            txtCodMaterial.setText(mat.getCodMaterial().toString());
            txtMaterial.setText(mat.getMaterial());
            txtTipo.setText(mat.getTipoMaterial().getSigla());
            try {
                if (loteMP.getMetodologia() != null) {
                    mtdsMap.add(loteMP.getMetodologia().getId());
                }
                if (loteMP.getMetodologia2() != null) {
                    mtdsMap.add(loteMP.getMetodologia2().getId());
                }
                if (loteMP.getMetodologia3() != null) {
                    mtdsMap.add(loteMP.getMetodologia3().getId());
                }
                if (!mtdsMap.isEmpty()) {
                    List<Metodologia> mtds = new MetodologiaDAO().findMetodologias(mtdsMap);
                    StringBuilder sb = new StringBuilder();
                    if (mtds != null) {
                        for (Metodologia mtd : mtds) {
                            if (!sb.toString().equals("")) {
                                sb.append("\n");
                            }
                            sb.append(mtd.getCodMetodo()).append(" - ").append(mtd.getMetodo());
                        }
                    }
                    txtMetodos.setText(sb.toString());
                } else {
                    JOptionPane.showMessageDialog(null, "Esse Lote não possui metodologia vinculada!",
                            "Metodologia não vinculada", JOptionPane.INFORMATION_MESSAGE);
                    Metodologia mtd = new Metodologia();
                    FrmProcurarMetodologia frmMtd = new FrmProcurarMetodologia(null, true, mtd,
                            new HashSet<>(material.getMetodologia()));
                    frmMtd.setVisible(true);
                    if (mtd != null) {
                        mtdsMap.add(mtd.getId());
                        txtMetodos.setText(mtd.getCodMetodo() + " - " + mtd.getMetodo());
                    }
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro");
            }
            txtLote.setText(loteMP.getLote());
            carregarData(
                    loteMP.getLoteInfo().getDataNecessidade(),
                    loteMP.getLoteInfo().getPrevLiberacao(),
                    loteMP.getLoteInfo().getDataStatus()
            );
            String[] status = {loteMP.getLoteInfo().getStatus()};
            status_lote = status[0];
            cmbStatus.setSelectedItem(status[0]);
            txtQuantidadeVolumes.setValue(loteMP.getLoteInfo().getQtdVolumes() == null ? 0
                    : loteMP.getLoteInfo().getQtdVolumes());
            txtObs.setText(loteMP.getLoteInfo().getObsCq());
            String[] unidade = {loteMP.getLoteInfo().getUnidade() == null ? "" : loteMP.getLoteInfo().getUnidade().getUnidade()};
            cmbUnidade.setSelectedItem(unidade[0]);
            txtQtdEstoque.setText(loteMP.getLoteInfo().getQtdEstoque() == null ? ""
                    : loteMP.getLoteInfo().getQtdEstoque().toString().replace(".", ","));
            txtDataVencimento.setDate(loteMP.getLoteInfo().getDataVenciamento());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar dados: " + ex);
        } finally {
            carregarAuditoria();
        }
        setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
    }

