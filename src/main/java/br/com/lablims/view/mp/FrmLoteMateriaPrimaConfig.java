    private void criar() {
        try {
            LoteMateriaPrimaInfo loteInfo = new LoteMateriaPrimaInfo();
            LoteMateriaPrima loteMP = new LoteMateriaPrima();
            PlanoAnaliseDAO paDAO = new PlanoAnaliseDAO();
            Material mat = new MaterialDAO().findMaterialByCodMaterial(Integer.valueOf(txtCodMaterial.getText()));
            loteMP.setMaterial(mat);
            if (mtdsMap.size() == 1) {
                if (mtdsMap.get(0) != null) {
                    loteMP.setMetodologia(new MetodologiaDAO().findById(Metodologia.class, mtdsMap.get(0)));
                }
            }
            if (mtdsMap.size() == 2) {
                if (mtdsMap.get(0) != null) {
                    loteMP.setMetodologia(new MetodologiaDAO().findById(Metodologia.class, mtdsMap.get(0)));
                }
                if (mtdsMap.get(1) != null) {
                    loteMP.setMetodologia2(new MetodologiaDAO().findById(Metodologia.class, mtdsMap.get(1)));
                }
            }
            if (mtdsMap.size() == 3) {
                if (mtdsMap.get(0) != null) {
                    loteMP.setMetodologia(new MetodologiaDAO().findById(Metodologia.class, mtdsMap.get(0)));
                }
                if (mtdsMap.get(1) != null) {
                    loteMP.setMetodologia2(new MetodologiaDAO().findById(Metodologia.class, mtdsMap.get(1)));
                }
                if (mtdsMap.get(2) != null) {
                    loteMP.setMetodologia3(new MetodologiaDAO().findById(Metodologia.class, mtdsMap.get(2)));
                }
            }
            loteMP.setLote(txtLote.getText().trim());
            loteMP.setVersion(0);
            loteInfo.setDataEntrada(txtDataEntrada.getDate() == null ? null
                    : DataHora.getDateTime(DataHora.getStringDate(txtDataEntrada.getDate())
                            + " " + txtHoraEntrada.getValue().toString()
                            + ":" + txtMinutoEntrada.getValue().toString() + ":00"));
            loteInfo.setDataNecessidade(txtDataNecessidade.getDate() == null ? null
                    : DataHora.getDateTime(DataHora.getStringDate(txtDataNecessidade.getDate())
                            + " " + txtHoraNecessidade.getValue().toString()
                            + ":" + txtMinutoNecessidade.getValue().toString() + ":00"));
            loteInfo.setPrevLiberacao(txtDataPrevisao.getDate() == null ? null
                    : DataHora.getDateTime(DataHora.getStringDate(txtDataPrevisao.getDate())
                            + " " + txtHoraPrevisao.getValue().toString()
                            + ":" + txtMinutoPrevisao.getValue().toString() + ":00"));
            if (!cmbStatus.getSelectedItem().toString().equals(status_lote)) {
                loteInfo.setDataStatus(new Date());
                loteInfo.setStatus(cmbStatus.getSelectedItem().toString());
            }
            loteInfo.setQtdVolumes((Integer) txtQuantidadeVolumes.getValue());
            loteInfo.setObsCq(txtObs.getText());
            loteInfo.setUnidade(new UnidadeMedidaDAO().findById(UnidadeMedida.class, 
                    ComboBox.getKeyForValue(cmbUnidade.getSelectedItem().toString(), categoryMapUnidade)));
            loteInfo.setQtdEstoque(new BigDecimal(txtQtdEstoque.getText().replaceAll("\\.", "").replaceAll(",", ".")));
            loteInfo.setDataVenciamento(txtDataVencimento.getDate());
            loteInfo.setVersion(0);
            loteMP = new LoteMateriaPrimaDAO().salvar(loteMP);
            if (!mtdsMap.isEmpty()) {
                for (Long key : mtdsMap) {
                    for (PlanoAnalise pa : paDAO.readPlanoAnalise(key)) {
                        LoteMateriaPrimaStatus loteStatus = new LoteMateriaPrimaStatus();
                        loteStatus.setLote(loteMP);
                        loteStatus.setPlanoAnalise(pa);
                        loteStatus.setAnaliseStatus(new AnaliseStatusDAO().findById(AnaliseStatus.class, 1L));
                        loteStatus.setVersion(0);
                        new LoteMateriaPrimaStatusDAO().salvar(loteStatus);
                    }
                }
            }
            loteInfo.setLote(loteMP);
            new LoteMateriaPrimaInfoDAO().salvarLoteMateriaPrimaInfo(loteInfo, loteMP);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao gravar dados: " + e);
        }
    }

