    private void criarDepartamento() {
        Departamento depart = new Departamento();
        DepartamentoDAO departamentoDAO = new DepartamentoDAO();
        try {
            depart.setSiglaDepartamento(txtSigla.getText().trim().toUpperCase());
            depart.setDepartamento(txtDepartamento.getText().trim().toUpperCase());
            depart.setDescricaoDepartamento(txtDescricao.getText().trim().toUpperCase());
            depart.setVersion(0);
            departamentoDAO.salvar(depart);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao gravar dados: " + e);
        }
    }

    private void atulizarDepartamento() {

        DepartamentoDAO departamentoDAO = new DepartamentoDAO();
        try {
            Departamento depart = departamentoDAO.findById(Departamento.class, departamento.getId());
            depart.setSiglaDepartamento(txtSigla.getText().trim().toUpperCase());
            depart.setDepartamento(txtDepartamento.getText().trim().toUpperCase());
            depart.setDescricaoDepartamento(txtDescricao.getText().trim().toUpperCase());
            depart.setVersion(departamento.getVersion() + 1);
            departamentoDAO.salvar(depart);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar dados: " + e);
        }
    }

    private void carregarDados() {
        try {
            txtSigla.setText(departamento.getSiglaDepartamento().toUpperCase());
            txtDepartamento.setText(departamento.getDepartamento().toUpperCase());
            txtDescricao.setText(departamento.getDescricaoDepartamento().toUpperCase());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar dados: " + ex);
        }
    }

    private void carregarCampos() {
        txtSigla.setText(null);
        txtDepartamento.setText(null);
        txtDescricao.setText(null);
        addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent e) {
                fechar();
            }
        });
    }

    private void fechar() {
        Frames.fecharFrame(this);
    }

