    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        try {
            if (UsuarioConfig == null || !UsuarioConfig.isVisible()) {
                UsuarioConfig = new FrmUsuarioConfig();
                UsuarioConfig.setVisible(true);
            } else if (UsuarioConfig.isVisible()) {
                UsuarioConfig.toFront();
                UsuarioConfig.show();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao abrir" + e);
        }
        setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
    }//GEN-LAST:event_jButton2ActionPerformed

