    private void carregar() {
        try {
            SistemaDAO sysDAO = new SistemaDAO();
            Sistema sys = sysDAO.findById(Sistema.class, 1L);
            txtNomePrograma.setText(sys.getSistemaNome());
            txtCriador.setText(sys.getSistemaCriador());
            txtDetalhes.setText(sys.getDetalhes());
            txtBancoDados.setText(sysDAO.infoBancoDados());
            txtDataVersao.setText(DataHora.getStringDateTime(sys.getSistemaDataUpdate()));
            try {
                StringBuilder sb = new StringBuilder();
                sb.append(sys.getMajorVersion()).append(".");
                sb.append(sys.getMinorVersion()).append(".");
                sb.append(sys.getPathVersion()).append(".");
                sb.append(sys.getQualifierVersion()).append(".");
                sb.append(sys.getBuilderVersao());
                txtVersaoPrograma.setText(sb.toString());
            } catch (Exception e) {
                txtVersaoPrograma.setText("0");
            }
            SegurancaDAO segDAO = new SegurancaDAO();
            for (Seguranca seg : segDAO.findEntities(Seguranca.class)) {
                switch (seg.getTipo()) {
                    case Seguranca.TAM_SENHA:
                        txtTamanhoSenha.setValue(seg.getNumero());
                        break;
                    case Seguranca.LETRAS:
                        btnSenhaLetras.setSelected(seg.getNumero() == 1);
                        if (btnSenhaLetras.isSelected()) {
                            btnSenhaLetras.setText("ON");
                            btnSenhaLetras.setSelected(true);
                            btnSenhaLetras.setBackground(Color.GREEN.darker());
                        } else {
                            btnSenhaLetras.setText("OFF");
                            btnSenhaLetras.setSelected(false);
                            btnSenhaLetras.setBackground(Color.RED.darker());
                        }
                        break;
                    case Seguranca.CRIPTO:
                        btnCriptografia.setSelected(seg.getNumero() == 1);
                        if (btnCriptografia.isSelected()) {
                            btnCriptografia.setText("ON");
                            btnCriptografia.setSelected(true);
                            btnCriptografia.setBackground(Color.GREEN.darker());
                        } else {
                            btnCriptografia.setText("OFF");
                            btnCriptografia.setSelected(false);
                            btnCriptografia.setBackground(Color.RED.darker());
                        }
                        break;
                    case Seguranca.MOTIVO:
                        btnMotivo.setSelected(seg.getNumero() == 1);
                        if (btnMotivo.isSelected()) {
                            btnMotivo.setText("ON");
                            btnMotivo.setSelected(true);
                            btnMotivo.setBackground(Color.GREEN.darker());
                        } else {
                            btnMotivo.setText("OFF");
                            btnMotivo.setSelected(false);
                            btnMotivo.setBackground(Color.RED.darker());
                        }
                        break;
                    case Seguranca.VALIDAR:
                        btnValidar.setSelected(seg.getNumero() == 1);
                        if (btnValidar.isSelected()) {
                            btnValidar.setText("ON");
                            btnValidar.setSelected(true);
                            btnValidar.setBackground(Color.GREEN.darker());
                        } else {
                            btnValidar.setText("OFF");
                            btnValidar.setSelected(false);
                            btnValidar.setBackground(Color.RED.darker());
                        }
                        break;
                    case Seguranca.EXPIRAR:
                        txtExpirarSenha.setValue(seg.getNumero());
                        break;
                    case Seguranca.FALHA:
                        txtFalhaAcesso.setValue(seg.getNumero());
                        break;
                    case Seguranca.FECHAR:
                        txtAutoLogoff.setValue(seg.getNumero());
                        break;
                }
            }
            carregarAuditoria();
        } catch (Exception e) {
            System.out.println(e.toString());
        }

    }

    private void atualizar() {
        SegurancaDAO segDAO = new SegurancaDAO();
        try {
            Seguranca seg1 = segDAO.findByTipo(Seguranca.TAM_SENHA);
            seg1.setNumero((Integer) txtTamanhoSenha.getValue());
            segDAO.salvar(seg1);

            Seguranca seg2 = segDAO.findByTipo(Seguranca.EXPIRAR);
            seg2.setNumero((Integer) txtExpirarSenha.getValue());
            segDAO.salvar(seg2);

            Seguranca seg3 = segDAO.findByTipo(Seguranca.FALHA);
            seg3.setNumero((Integer) txtFalhaAcesso.getValue());
            segDAO.salvar(seg3);

            Seguranca seg4 = segDAO.findByTipo(Seguranca.FECHAR);
            seg4.setNumero((Integer) txtAutoLogoff.getValue());
            segDAO.salvar(seg4);

            Seguranca seg5 = segDAO.findByTipo(Seguranca.CRIPTO);
            seg5.setNumero(btnCriptografia.isSelected() ? 1 : 0);
            segDAO.salvar(seg5);

            Seguranca seg6 = segDAO.findByTipo(Seguranca.MOTIVO);
            seg6.setNumero(btnMotivo.isSelected() ? 1 : 0);
            segDAO.salvar(seg6);

            Seguranca seg7 = segDAO.findByTipo(Seguranca.VALIDAR);
            seg7.setNumero(btnValidar.isSelected() ? 1 : 0);
            segDAO.salvar(seg7);

            Seguranca seg8 = segDAO.findByTipo(Seguranca.LETRAS);
            seg8.setNumero(btnSenhaLetras.isSelected() ? 1 : 0);
            segDAO.salvar(seg8);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao gravar dados: " + e);
        }
    }

