    private void carregarAuditoria() {
        DefaultTableModel model = (DefaultTableModel) tblAuditoria.getModel();
        SegurancaDAO segDAO = new SegurancaDAO();
        model.setNumRows(0);
        try {
            for (Seguranca seg : segDAO.readSegurancaAuditoria()) {
                model.addRow(new Object[]{
                    seg.getId(),
                    seg.getVersion(),
                    seg.getAudit().getMOD() == 0 ? "Criado" : (seg.getAudit().getMOD() == 1 ? "Modificado" : "Removido"),
                    seg.getTipo(),
                    seg.getNumero(),
                    seg.getNumero_MOD() == false ? "Não Alterado" : "Alterado",
                    seg.getAudit().getComputador() + " / " + seg.getAudit().getUserComputador(),
                    seg.getAudit().getUltimaModificacaoPor(),
                    DataHora.getStringDateTime(seg.getAudit().getUltimaModificacao()),
                    seg.getAudit().getMotivo()
                });
            }
        } catch (Exception e) {
        }
    }

