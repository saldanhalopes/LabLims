    private void carregarStorage() {
        cmbStorage.removeAllItems();
        cmbStorage.addItem("");
        categoryMapStorage.clear();
        ColunaStorageDAO colStorageDAO = new ColunaStorageDAO();
        try {
            for (ColunaStorage colStorage : colStorageDAO.findAll()) {
                Long id = colStorage.getId();
                String name = colStorage.getSetor().getSiglaSetor()
                        + " - " + colStorage.getTipo()
                        + " - " + colStorage.getNumero();
                cmbStorage.addItem(name);
                categoryMapStorage.put(id, name);
            }
        } catch (Exception e) {
        }
    }

    private void carregarDados() {
        ColunaVagaDAO colVagaDAO = new ColunaVagaDAO();
        try {
            colunaVaga = colVagaDAO.findById(ColunaVaga.class, colunaVaga.getId());
            String[] storage = {
                colunaVaga.getColunaStorage().getSetor().getSiglaSetor()
                + " - " + colunaVaga.getColunaStorage().getTipo()
                + " - " + colunaVaga.getColunaStorage().getNumero()};
            cmbStorage.setSelectedItem(storage[0]);
            txtVaga.setValue(colunaVaga.getVaga());
            txtObs.setText(colunaVaga.getObs());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar dados: " + e);
        }
    }

    private void atualizarVaga() {
        Integer vaga = 1;
        ColunaVagaDAO colVagaDAO = new ColunaVagaDAO();
        try {
            vaga = colVagaDAO.getUltimoNumeroColunaVaga(ComboBox.getKeyForValue(cmbStorage.getSelectedItem().toString(), categoryMapStorage)) + 1;
        } catch (Exception e) {
            vaga = 1;
        }
        txtVaga.setValue(vaga);
    }

    private void salvar() {
        Senha senha = new Senha();
        Boolean vaga = false;
        ColunaVagaDAO colVagaDAO = new ColunaVagaDAO();
        try {
            if (novo) {
                vaga = colVagaDAO.checkNumeroColunaVaga(ComboBox.getKeyForValue(cmbStorage.getSelectedItem().toString(), categoryMapStorage),
                        (Integer) txtVaga.getValue());
            } else {
                if (!colunaVaga.getVaga().equals((Integer) txtVaga.getValue())
                        || !(colunaVaga.getColunaStorage().getId())
                        .equals(ComboBox.getKeyForValue(cmbStorage.getSelectedItem().toString(), categoryMapStorage))) {
                    vaga = colVagaDAO.checkNumeroColunaVaga(ComboBox.getKeyForValue(cmbStorage.getSelectedItem().toString(), categoryMapStorage),
                            (Integer) txtVaga.getValue());
                }
            }
        } catch (Exception e) {
            vaga = false;
        }
        try {
            if (cmbStorage.getSelectedItem().toString().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Storage inválido");
                cmbStorage.requestFocus();
            } else if (vaga) {
                JOptionPane.showMessageDialog(null, "Número da Vaga já existe!");
                txtVaga.requestFocus();
            } else if (novo) {
                if (senha.Salvar()) {
                    setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
                    criar();
                    setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
                    dispose();
                }
            } else {
                if (senha.Salvar()) {
                    setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
                    atulizar();
                    setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
                    dispose();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar dados: " + e);
        }
    }

    private void criar() {
        try {
            ColunaVaga colVaga = new ColunaVaga();
            ColunaVagaDAO colVagaDAO = new ColunaVagaDAO();
            colVaga.setColunaStorage(new ColunaStorageDAO().findById(ColunaStorage.class,
                    ComboBox.getKeyForValue(cmbStorage.getSelectedItem().toString(), categoryMapStorage)));
            colVaga.setObs(txtObs.getText().toUpperCase().trim());
            colVaga.setVaga((Integer) txtVaga.getValue());
            colVaga.setVersion(0);
            colVagaDAO.salvar(colVaga);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao criar dados: " + ex);
        }
    }

    private void atulizar() {
        try {
            ColunaVagaDAO colVagaDAO = new ColunaVagaDAO();
            colunaVaga.setColunaStorage(new ColunaStorageDAO().findById(ColunaStorage.class,
                    ComboBox.getKeyForValue(cmbStorage.getSelectedItem().toString(), categoryMapStorage)));
            colunaVaga.setObs(txtObs.getText().toUpperCase().trim());
            colunaVaga.setVaga((Integer) txtVaga.getValue());
            colunaVaga.setVersion(colunaVaga.getVersion() + 1);
            colVagaDAO.salvar(colunaVaga);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao criar dados: " + ex);
        }
    }

