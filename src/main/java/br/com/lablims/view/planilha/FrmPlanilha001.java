    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Calcular();
        int dialogResult = JOptionPane.showConfirmDialog(null,
                "Deseja Imprimir Relatorio de Troca de Turno ?", "Aviso", JOptionPane.YES_NO_OPTION);
        if (dialogResult == 0) {
            try {
                InputStream jasperFile = Thread.currentThread().getClass().getResourceAsStream("/Reports/CQ_PE_0001.jasper");
                JasperReport jasperReport = (JasperReport) JRLoader.loadObject(jasperFile);
                JasperPrint jpt = JasperFillManager.fillReport(jasperReport, Map(), new JREmptyDataSource());
                JasperViewer jv = new JasperViewer(jpt, false);
                jv.setLocationRelativeTo(null);
                jv.setVisible(true);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro ao chamar o relatório!\nErro: " + ex);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

