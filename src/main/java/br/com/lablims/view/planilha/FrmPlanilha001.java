    private void Carregar() {
        Integer qtdeCasasDecimais = (Integer) SpnNumeroCasas.getValue();
        Set newKeys = new HashSet();
        newKeys.add(KeyStroke.getKeyStroke(KeyEvent.VK_ENTER, 0));
        JPanel[] pnls = {pnlValores1, pnlValores2, pnlValores3};
        for (JPanel pnl : pnls) {
            for (Component c : pnl.getComponents()) {
                if (c instanceof JFormattedTextField) {
                    JFormattedTextField f = (JFormattedTextField) c;
                    f.setFormatterFactory(DecimalFormattedField.DecimalFormattedField(qtdeCasasDecimais));
                    f.setInputVerifier(DecimalFormattedField.Verifier());
                    f.setFocusTraversalKeys(KeyboardFocusManager.FORWARD_TRAVERSAL_KEYS, newKeys);
                    f.addFocusListener(new FocusListener() {
                        @Override
                        public void focusGained(FocusEvent e) {
                            Calcular();
                        }
                        @Override
                        public void focusLost(FocusEvent e) {
                            Calcular();
                        }
                    });
                }
            }
        }
    }

