    private boolean calcular() {
        int campo1 = (Integer) txtQtCanalB.getValue();
        int campo2 = (Integer) txtQtCanalC.getValue();
        int campo3 = (Integer) txtQtCanalD.getValue();
        int campo4 = 100 - campo1 - campo2 - campo3;
        if (campo4 < 0) {
            return false;
        } else {
            txtQtCanalA.setText(String.valueOf(campo4));
            return true;
        }
    }

    public void criarFaseMovelCromatografo() {
        try {
            //Criar Registo
            FaseMovelLogDAO faseLogDAO = new FaseMovelLogDAO();
            Equipamento equip = new EquipamentoDAO().findByTag(sys_name);
            Usuario user = new UsuarioDAO().findByName(System.getProperty("user"));
            Campanha camp = new CampanhaDAO().findById(Campanha.class, campanha_id);
            FaseMovelLog faseLog = new FaseMovelLog();
            faseLog.setFase_movel_a(txtCanalA.getText().toUpperCase());
            faseLog.setFase_movel_a_qt(Integer.parseInt(txtQtCanalA.getText()));
            faseLog.setFase_movel_a_lote(txtLoteCanalA.getText().toUpperCase());
            faseLog.setFase_movel_a_validade(txtValidadeCanalA.getDate());
            faseLog.setFase_movel_b(txtCanalB.getText().toUpperCase());
            faseLog.setFase_movel_b_qt((Integer) txtQtCanalB.getValue());
            faseLog.setFase_movel_b_lote(txtLoteCanalB.getText().toUpperCase());
            faseLog.setFase_movel_b_validade(txtValidadeCanalB.getDate());
            faseLog.setFase_movel_c(txtCanalC.getText().toUpperCase());
            faseLog.setFase_movel_c_qt((Integer) txtQtCanalC.getValue());
            faseLog.setFase_movel_c_lote(txtLoteCanalC.getText().toUpperCase());
            faseLog.setFase_movel_c_validade(txtValidadeCanalC.getDate());
            faseLog.setFase_movel_d(txtCanalD.getText().toUpperCase());
            faseLog.setFase_movel_d_qt((Integer) txtQtCanalD.getValue());
            faseLog.setFase_movel_d_lote(txtLoteCanalD.getText().toUpperCase());
            faseLog.setFase_movel_d_validade(txtValidadeCanalD.getDate());
            faseLog.setFase_movel_e(txtCanalE.getText().toUpperCase());
            faseLog.setFase_movel_e_lote(txtLoteCanalE.getText().toUpperCase());
            faseLog.setFase_movel_e_validade(txtValidadeCanalE.getDate());
            faseLog.setFase_movel_f(txtCanalF.getText().toUpperCase());
            faseLog.setFase_movel_f_lote(txtLoteCanalF.getText().toUpperCase());
            faseLog.setFase_movel_f_validade(txtValidadeCanalF.getDate());
            faseLog.setDataRegistro(new Date());
            faseLog.setCampanha(camp);
            faseLog.setEquipamento(equip);
            faseLog.setUsuario(user);
            //Salva Reistro
            faseLogDAO.salvar(faseLog);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao gravar dados: " + e);
        }
    }

    public void criarRegistroCromatografo() {
        try {
            Date datahora_inicio = new LogCromatografoDAO().findLastDataInicio(campanha_id);
            Date datahora_fim = DataHora.getTimestampDateTime(DataHora.getStringDate(txtDataAtualRegisto.getDate())
                    + " " + txtHoraAtualRegisto.getValue().toString()
                    + ":" + txtMinutoAtualRegisto.getValue().toString() + ":00");
            LogCromatografo log = new LogCromatografo();
            LogCromatografoDAO logDAO = new LogCromatografoDAO();
            log.setDescricao("FASE MÓVEL");
            log.setAnaliseStatus(new AnaliseStatusDAO().findByStatus("TROCA DE FASE MÓVEL"));
            log.setCampanha(new CampanhaDAO().findById(Campanha.class, campanha_id));
            log.setDataFim(datahora_fim);
            log.setDataInicio(datahora_inicio);
            log.setDataRegistro(new Date());
            log.setObs("SUBSTITUIÇÃO DA FASE MÓVEL");
            log.setUsuario(new UsuarioDAO().findByName(System.getProperty("user")));
            logDAO.salvar(log);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao gravar dados: " + e);
        }

    }

