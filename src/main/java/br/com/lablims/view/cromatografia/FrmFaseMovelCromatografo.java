        txtQtCanalC.setEditor(editorC);
        SpinnerNumberModel modelD = new SpinnerNumberModel(0, 0, 100, 1);
        txtQtCanalD.setModel(modelD);
        JSpinner.NumberEditor editorD = new JSpinner.NumberEditor(txtQtCanalD);
