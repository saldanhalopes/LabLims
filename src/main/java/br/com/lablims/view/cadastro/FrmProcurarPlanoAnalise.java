    private void read() {
        DefaultTableModel model = (DefaultTableModel) tblPlanoAnalise.getModel();
        PlanoAnaliseDAO paDAO = new PlanoAnaliseDAO();
        model.setNumRows(0);
        try {
            for (Object[] objects : paDAO.readPlanosAnalises()) {
                Analise ansl = new AnaliseDAO().findById(Analise.class, (Long) objects[1]);
                AnaliseTipo anslTipo = new AnaliseTipoDAO().findById(AnaliseTipo.class, (Long) objects[2]);
                Setor setor = new SetorDAO().findById(Setor.class, (Long) objects[3]);
                model.addRow(new Object[]{
                    objects[0].toString(),
                    ansl.getAnalise(),
                    anslTipo.getAnaliseTipo(),
                    setor.getSetor(),
                    objects[4]
                });
            }
        } catch (Exception ex) {
            Logger.getLogger(FrmProcurarPlanoAnalise.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void carregar() {
        planoAnalise.setDescricao(tblPlanoAnalise.getValueAt(tblPlanoAnalise.getSelectedRow(), 0).toString());
        planoAnalise.setAnalise(new Analise(tblPlanoAnalise.getValueAt(tblPlanoAnalise.getSelectedRow(), 1).toString()));
        planoAnalise.setAnaliseTipo(new AnaliseTipo(tblPlanoAnalise.getValueAt(tblPlanoAnalise.getSelectedRow(), 2).toString()));
        planoAnalise.setSetor(new Setor(tblPlanoAnalise.getValueAt(tblPlanoAnalise.getSelectedRow(), 3).toString()));
        dispose();
    }

