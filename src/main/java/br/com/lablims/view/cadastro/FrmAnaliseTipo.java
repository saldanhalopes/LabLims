    private void tblAnaliseTipoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAnaliseTipoMouseClicked
        if (evt.getClickCount() == 2) {
            if (tblAnaliseTipo.getSelectedRow() != -1) {
                atualizar();
            }
        }
    }//GEN-LAST:event_tblAnaliseTipoMouseClicked

    private void tblAnaliseTipoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblAnaliseTipoKeyReleased
        if (tblAnaliseTipo.getSelectedRow() != -1) {
            if (evt.getKeyCode() == KeyEvent.VK_DELETE) {
                deletar();
            }
        }
    }//GEN-LAST:event_tblAnaliseTipoKeyReleased

