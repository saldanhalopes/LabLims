    public final void readAnaliseTipo() {
        EntityManager em = ConnectionFactory.em(true);
        DefaultTableModel model = (DefaultTableModel) tblAnaliseTipo.getModel();
        AnaliseTipoDAO analise_tipoDAO = new AnaliseTipoDAO();
        model.setNumRows(0);
        try {
            for (AnaliseTipo analise_tipo : analise_tipoDAO.findEntities(em, AnaliseTipo.class)) {
                model.addRow(new Object[]{
                    analise_tipo.getId(),
                    analise_tipo.getSiglaAnaliseTipo(),
                    analise_tipo.getAnaliseTipo(),
                    analise_tipo.getDescricaoAnaliseTipo(),
                    analise_tipo.getVersion()
                });
            }
            carregarAuditoria();
        } catch (Exception e) {
        } finally {
            em.close();
        }
    }

    private void abrir() {
        try {
            new FrmAnaliseTipoDados(null, true).setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar dados: " + e);
        } finally {
            readAnaliseTipo();
        }
    }

    private void atualizar() {
        EntityManager em = ConnectionFactory.em(true);
        AnaliseTipoDAO analise_tipoDAO = new AnaliseTipoDAO();
        try {
            setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
            AnaliseTipo analise_tipo = analise_tipoDAO.findById(em, AnaliseTipo.class, (Integer) tblAnaliseTipo.getValueAt(tblAnaliseTipo.getSelectedRow(), 0));
            new FrmAnaliseTipoDados(null, true, analise_tipo).setVisible(true);
            setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar dados: " + e);
        } finally {
            em.close();
            readAnaliseTipo();
        }
    }

    private void deletar() {
        EntityManager em = ConnectionFactory.em(true);
        AnaliseTipoDAO analise_tipoDAO = new AnaliseTipoDAO();
        Senha senha = new Senha();
        try {
            int dialogResult = JOptionPane.showConfirmDialog(null, "Realmente deseja Excluir esse registro?", "Aviso", JOptionPane.YES_NO_OPTION);
            if (dialogResult == 0) {
                FrmSalvar frmSalvar = new FrmSalvar(null, true, em, senha);
                frmSalvar.setVisible(true);
                if (senha.getSenha()) {
                    AnaliseTipo analise_tipo = analise_tipoDAO.findById(em, AnaliseTipo.class, (Integer) tblAnaliseTipo.getValueAt(tblAnaliseTipo.getSelectedRow(), 0));
                    analise_tipoDAO.remover(em, AnaliseTipo.class, analise_tipo.getId());
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao atulalizar dados: " + e);
        } finally {
            em.close();
            readAnaliseTipo();
        }
    }

    private void carregarAuditoria() {
        EntityManager em = ConnectionFactory.em(true);
        DefaultTableModel model = (DefaultTableModel) tblAnaliseTipoAuditoria.getModel();
        AnaliseTipoDAO analise_tipoDAO = new AnaliseTipoDAO();
        model.setNumRows(0);
        try {
            for (AnaliseTipo analise_tipo : analise_tipoDAO.readAnaliseTipoAuditoria()) {
                model.addRow(new Object[]{
                    analise_tipo.getId(),
                    analise_tipo.getVersion(),
                    analise_tipo.getMOD() == 0 ? "Criado" : (analise_tipo.getMOD() == 1 ? "Modificado" : "Removido"),
                    analise_tipo.getAnaliseTipo(),
                    analise_tipo.getAnaliseTipo_MOD() == 0 ? "Não Alterado" : "Alterado",
                    analise_tipo.getSiglaAnaliseTipo(),
                    analise_tipo.getSigla_AnaliseTipo_MOD() == 0 ? "Não Alterado" : "Alterado",
                    analise_tipo.getDescricaoAnaliseTipo(),
                    analise_tipo.getDescricao_AnaliseTipo_MOD() == 0 ? "Não Alterado" : "Alterado",
                    analise_tipo.getAudit().getComputador() + " / "
                    + analise_tipo.getAudit().getUserComputador(),
                    analise_tipo.getAudit().getUltimaModificacaoPor(),
                    DataHora.getStringDateTime(analise_tipo.getAudit().getUltimaModificacao()),
                    analise_tipo.getAudit().getMotivo()
                });
            }
        } catch (Exception e) {
        } finally {
            em.close();
        }
    }

