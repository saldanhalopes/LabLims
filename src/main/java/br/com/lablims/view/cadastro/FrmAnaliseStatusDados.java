    private void criar() {
        AnaliseStatus anlsStatus = new AnaliseStatus();
        AnaliseStatusDAO analiseStatusDAO = new AnaliseStatusDAO();
        try {
            anlsStatus.setSiglaAnaliseStatus(txtSiglaStatus.getText().trim().toUpperCase());
            anlsStatus.setAnaliseStatus(txtStatus.getText().trim().toUpperCase());
            anlsStatus.setDescricaoAnaliseStatus(txtDescricao.getText().trim().toUpperCase());
            anlsStatus.setVersion(0);
            analiseStatusDAO.salvar(anlsStatus);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao gravar dados: " + e);
        }
    }

    private void atulizar() {
        AnaliseStatus anlsStatus = new AnaliseStatus();
        AnaliseStatusDAO analiseStatusDAO = new AnaliseStatusDAO();
        try {
            anlsStatus.setId(analiseStatus.getId());
            anlsStatus.setSiglaAnaliseStatus(txtSiglaStatus.getText().trim().toUpperCase());
            anlsStatus.setAnaliseStatus(txtStatus.getText().trim().toUpperCase());
            anlsStatus.setDescricaoAnaliseStatus(txtDescricao.getText().trim().toUpperCase());
            anlsStatus.setVersion(analiseStatus.getVersion() + 1);
            analiseStatusDAO.salvar(anlsStatus);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar dados: " + e);
        }
    }

    private void carregarDados() {
        try {
            txtSiglaStatus.setText(analiseStatus.getSiglaAnaliseStatus().toUpperCase());
            txtStatus.setText(analiseStatus.getAnaliseStatus().toUpperCase());
            txtDescricao.setText(analiseStatus.getDescricaoAnaliseStatus().toUpperCase());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar dados: " + ex);
        }
    }

    private void carregarCampos() {
        txtSiglaStatus.setText(null);
        txtStatus.setText(null);
        txtDescricao.setText(null);
        addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent e) {
                fechar();
            }
        });
    }

