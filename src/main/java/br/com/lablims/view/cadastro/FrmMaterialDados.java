    private void readControlado() {
        cmbFiscalizado.removeAll();
        cmbFiscalizado.addItem("");
        cmbFiscalizado.addItem("PolÃ­cia Civil");
        cmbFiscalizado.addItem("PolÃ­cia Federal");
        cmbFiscalizado.addItem("ExÃ©rcito");
    }

    private void readTipo() {
        cmbTipo.removeAllItems();
        cmbTipo.addItem("");
        categoryMapTipo.clear();
        TipoMaterialDAO tipoMaterialDAO = new TipoMaterialDAO();
        try {
            for (TipoMaterial tipoMaterial : tipoMaterialDAO.findEntities(TipoMaterial.class, "tipo", true)) {
                Long id = tipoMaterial.getId();
                String name = tipoMaterial.getTipo();
                cmbTipo.addItem(name);
                categoryMapTipo.put(id, name);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar dados: " + e);
        }
    }

    private void criarMaterial() {
        Material mat = new Material();
        MaterialDAO matDAO = new MaterialDAO();
        try {
            mat.setCodMaterial(Integer.parseInt(txtCodMaterial.getText().trim()));
            mat.setMaterial(txtMaterial.getText().trim().toUpperCase());
            mat.setTipoMaterial(new TipoMaterialDAO().findById(TipoMaterial.class, ComboBox.getKeyForValue(cmbTipo.getSelectedItem().toString(), categoryMapTipo)));
            mat.setControleEspecial(!cmbFiscalizado.getSelectedItem().toString().equals("NÃ£o"));
            mat.setFiscalizado(cmbFiscalizado.getSelectedItem().toString());
            mat.setVersion(0);
            matDAO.salvar(mat);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao gravar dados: " + e);
        }
    }

    private void atulizarMaterial() {
        MaterialDAO matDAO = new MaterialDAO();
        try {
            material.setCodMaterial(Integer.parseInt(txtCodMaterial.getText().trim()));
            material.setMaterial(txtMaterial.getText().trim().toUpperCase());
            material.setTipoMaterial(new TipoMaterialDAO().findById(TipoMaterial.class, ComboBox.getKeyForValue(cmbTipo.getSelectedItem().toString(), categoryMapTipo)));
            material.setControleEspecial(!cmbFiscalizado.getSelectedItem().toString().equals("NÃ£o"));
            material.setFiscalizado(cmbFiscalizado.getSelectedItem().toString());
            material.setVersion(material.getVersion() + 1);
            matDAO.salvar(material);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar dados: " + e);
        }
    }

    private void carregarDados() {
        try {
            txtCodMaterial.setText(material.getCodMaterial().toString());
            txtMaterial.setText(material.getMaterial().toUpperCase());
            String[] tipo = {material.getTipoMaterial().getTipo()};
            cmbTipo.setSelectedItem(tipo[0]);
            String[] controlado = {material.getFiscalizado()};
            cmbFiscalizado.setSelectedItem(controlado[0]);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar dados: " + ex);
        }
    }

    private void limparCampos() {
        txtCodMaterial.setText(null);
        txtMaterial.setText(null);
        cmbTipo.setSelectedItem(0);
        txtCodMaterial.setDocument(new FieldFormatLimit(9, FieldFormatLimit.NUMEROS));
    }

    private void fechar() {
        Frames.fecharFrame(this);
    }

