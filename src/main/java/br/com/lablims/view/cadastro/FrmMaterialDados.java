    private void btnSalvarMetodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarMetodoActionPerformed
        MaterialDAO matDAO = new MaterialDAO();
        Senha senha = new Senha();
        try {
            if (txtCodMaterial.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "CÃ³digo invÃ¡lido");
                txtCodMaterial.requestFocus();
            } else if (txtMaterial.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Material invÃ¡lido");
                txtMaterial.requestFocus();
            } else if (cmbTipo.getSelectedItem().toString().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Tipo invÃ¡lido");
                cmbTipo.requestFocus();
            } else if (novoMaterial) {
                if (matDAO.checkCodMaterialIsExits(Integer.parseInt(txtCodMaterial.getText().trim()))) {
                    JOptionPane.showMessageDialog(null, "Material jÃ¡ existente");
                    txtCodMaterial.requestFocus();
                } else {
                    if (senha.Salvar()) {
                        setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
                        criarMaterial();
                        setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
                        dispose();
                    }
                }
            } else {
                if (senha.Salvar()) {
                    setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
                    atulizarMaterial();
                    setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
                    dispose();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao gravar dados: " + e);
        }
    }//GEN-LAST:event_btnSalvarMetodoActionPerformed

